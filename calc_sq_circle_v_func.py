
def enter_length_x_width(length, width):
    """Calculates square footage of a house"""

    sq_ft = length*width
    print(f"A house that is {length} feet long and {width} wide has {sq_ft} square feet.")


def enter_radius(radius):
    """Calculates circumference of circle when given the radius"""

    circ_r = 2*3.14*radius
    print(f"The circumference of a circle with the radius {radius} is {circ_r}.")

